class Solution:
    # V0: WITHOUT DP --> GOT TLE AS EXPECTED. ADDING MEMO
    # V1: WITH DP. ALTHOUGH CODE LOOKS SIMILAR IT COMPLETELY
    # DIFFERENT. RETURNING REMAINING PROFIT FROM THIS POINT,
    # NOT TOTAL PROFIT UP TO THIS POINT. USE THIS FOR Coin Change
    def __init__(self):
        self.max_prof = {}
    
    def maxProfit(self, prices: List[int]) -> int:
        return self.profit(prices, 0, True)
        
    # For DP profit so far is not the right thing. Calculate
    # remaining profit.
    def profit(self, prices: List[int], i: int, can_buy: bool) -> int:
        if i >= len(prices):
            return 0 # remaining profit

        if (i, can_buy) in self.max_prof:
            return self.max_prof[(i, can_buy)]

        cool = self.profit(prices, i+1, can_buy)
        if can_buy:
            buy = self.profit(prices, i+1, False)-prices[i]
            self.max_prof[(i, can_buy)] = max(cool, buy)
        else:
            sell = self.profit(prices, i+2, True)+prices[i]
            self.max_prof[(i, can_buy)] = max(cool, sell)
        
        return self.max_prof[(i, can_buy)]



